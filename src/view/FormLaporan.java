    private void tampilkanLaporanKunjungan() {
        try {
            Date awal = parseTanggal(txtAwal1.getText());
            Date akhir = parseTanggal(txtAkhir1.getText());
            Integer idDokter = resolveIdDokter(txtDokter1.getText());
            String status = txtstatus1.getText() == null ? "" : txtstatus1.getText().trim();

            List<Object[]> data = laporanDao.getKunjunganPerPeriode(awal, akhir, idDokter, status);
            DefaultTableModel model = (DefaultTableModel) tbl1.getModel();
            model.setRowCount(0);
            for (Object[] row : data) {
                model.addRow(row);
            }
            lblTotal1.setText("Total Kunjungan: " + data.size());
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Validasi", JOptionPane.WARNING_MESSAGE);
        }
    }

    // =====================================================================
    // 2. LAPORAN PENDAPATAN PER PERIODE
    // =====================================================================
    private void tampilkanLaporanPendapatan() {
        try {
            String awal = txtAwal2.getText() == null ? "" : txtAwal2.getText().trim();
            String akhir = txtAkhir2.getText() == null ? "" : txtAkhir2.getText().trim();
            if (!awal.isEmpty()) {
                parseTanggal(awal);
            }
            if (!akhir.isEmpty()) {
                parseTanggal(akhir);
            }

            List<Object[]> data = laporanDao.getPendapatanPerPeriode(awal, akhir);
            DefaultTableModel model = (DefaultTableModel) tbl2.getModel();
            model.setRowCount(0);
            for (Object[] row : data) {
                model.addRow(new Object[]{
                    row[0], row[1], row[2], row[3],
                    formatRupiah((BigDecimal) row[4]),
                    formatRupiah((BigDecimal) row[5]),
                    formatRupiah((BigDecimal) row[6])
                });
            }
            BigDecimal[] total = laporanDao.getTotalPendapatan(awal, akhir);
            lblTotal2.setText("Total Tindakan: " + formatRupiah(total[0])
                    + "   |   Total Obat: " + formatRupiah(total[1])
                    + "   |   Total Pendapatan: " + formatRupiah(total[2]));
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Validasi", JOptionPane.WARNING_MESSAGE);
        }
    }

