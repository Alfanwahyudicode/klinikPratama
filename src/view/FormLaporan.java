    /** Parse teks tanggal (format yyyy-MM-dd). Mengembalikan null jika kosong. */
    private Date parseTanggal(String input) {
        if (input == null || input.trim().isEmpty()) {
            return null;
        }
        try {
            return Date.valueOf(input.trim());
        } catch (IllegalArgumentException e) {
            throw new IllegalArgumentException("Format tanggal salah: \"" + input.trim() + "\". Gunakan format yyyy-MM-dd, contoh: 2025-01-31.");
        }
    }

    /**
     * Mengubah isi txtDokter1 menjadi id_dokter. Boleh diisi angka (ID) atau
     * sebagian nama dokter. Mengembalikan null jika input kosong (artinya:
     * semua dokter).
     */
    private Integer resolveIdDokter(String input) {
        if (input == null || input.trim().isEmpty()) {
            return null;
        }
        String value = input.trim();
        if (value.matches("\\d+")) {
            return Integer.parseInt(value);
        }
        List<Dokter> hasil = dokterDao.cariDokter(value);
        if (hasil.isEmpty()) {
            throw new IllegalArgumentException("Dokter dengan ID/nama \"" + value + "\" tidak ditemukan.");
        }
        return hasil.get(0).getIdDokter();
    }

    private String formatRupiah(BigDecimal value) {
        if (value == null) {
            value = BigDecimal.ZERO;
        }
        return "Rp " + RUPIAH.format(value);
    }

    /** Export isi sebuah JTable (berdasarkan DefaultTableModel-nya) ke file CSV. */
    private void exportTableToCsv(JTable table, String namaDasarFile) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Tidak ada data untuk diexport. Tampilkan data terlebih dahulu.",
                    "Info", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        JFileChooser chooser = new JFileChooser();
        chooser.setSelectedFile(new File(namaDasarFile + "_" + SDF.format(new java.util.Date()) + ".csv"));
        int hasil = chooser.showSaveDialog(this);
        if (hasil != JFileChooser.APPROVE_OPTION) {
            return;
        }
        File file = chooser.getSelectedFile();
        try (PrintWriter pw = new PrintWriter(new FileWriter(file))) {
            for (int c = 0; c < model.getColumnCount(); c++) {
                pw.print(csvEscape(model.getColumnName(c)));
                if (c < model.getColumnCount() - 1) {
                    pw.print(",");
                }
            }
            pw.println();
            for (int r = 0; r < model.getRowCount(); r++) {
                for (int c = 0; c < model.getColumnCount(); c++) {
                    Object val = model.getValueAt(r, c);
                    pw.print(csvEscape(val == null ? "" : val.toString()));
                    if (c < model.getColumnCount() - 1) {
                        pw.print(",");
                    }
                }
                pw.println();
            }
            JOptionPane.showMessageDialog(this, "Data berhasil diexport ke:\n" + file.getAbsolutePath());
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Gagal export data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void exportTextToFile(String content, String namaDasarFile) {
        JFileChooser chooser = new JFileChooser();
        chooser.setSelectedFile(new File(namaDasarFile + ".txt"));
        int hasil = chooser.showSaveDialog(this);
        if (hasil != JFileChooser.APPROVE_OPTION) {
            return;
        }
        File file = chooser.getSelectedFile();
        try (PrintWriter pw = new PrintWriter(new FileWriter(file))) {
            pw.print(content);
            JOptionPane.showMessageDialog(this, "Riwayat berhasil diexport ke:\n" + file.getAbsolutePath());
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Gagal export riwayat: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private String csvEscape(String s) {
        if (s.contains(",") || s.contains("\"") || s.contains("\n")) {
            return "\"" + s.replace("\"", "\"\"") + "\"";
        }
        return s;
    }

