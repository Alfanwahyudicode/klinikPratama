    private void setupTambahan() {
        String tipTanggal = "Format: yyyy-MM-dd (boleh dikosongkan)";
        txtAwal1.setToolTipText(tipTanggal);
        txtAkhir1.setToolTipText(tipTanggal);
        txtAwal2.setToolTipText(tipTanggal);
        txtAkhir2.setToolTipText(tipTanggal);
        txtAwal3.setToolTipText(tipTanggal);
        txtAkhir3.setToolTipText(tipTanggal);
        txtDokter1.setToolTipText("ID atau nama dokter (boleh dikosongkan)");
        txtstatus1.setToolTipText("Status kunjungan, contoh: selesai / menunggu / batal (boleh dikosongkan)");
        txtAwal4.setToolTipText("Masukkan ID Pasien atau sebagian nama pasien");
        txtAkhir4.setToolTipText("Tidak digunakan pada pencarian ini");
        txtAkhir4.setEnabled(false);

        jButton4.addActionListener(evt -> exportTableToCsv(tbl2, "laporan_pendapatan"));
        jButton6.addActionListener(evt -> exportTableToCsv(tbl3, "laporan_obat_terpakai"));
        jButton8.addActionListener(evt -> exportTableToCsv(tbl4, "hasil_pencarian_pasien"));

        tbl4.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() == 2) {
                    int row = tbl4.getSelectedRow();
                    if (row >= 0) {
                        int idPasien = Integer.parseInt(String.valueOf(tbl4.getValueAt(row, 0)));
                        String nama = String.valueOf(tbl4.getValueAt(row, 1));
                        tampilkanRiwayatLengkap(idPasien, nama);
                    }
                }
            }
        });
        lblTotal4.setText("Ketikkan ID/nama pasien lalu klik \"Cari Pasien\". Klik dua kali baris hasil untuk melihat riwayat lengkap.");
    }

